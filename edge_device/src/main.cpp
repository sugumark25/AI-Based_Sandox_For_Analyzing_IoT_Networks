// ?????? Forward declarations ???????????????????????????????????????????????????????????????????????????????????????
void _blinkAlert(int times);
void _printHeartbeat(unsigned long now);

// ?????? External globals ???????????????????????????????????????????????????????????????????????????????????????????????????
extern PersistentPatternCache patternCache;
extern WiFiClient wifiClient;
extern PubSubClient mqttClient;
extern SensorReading latestReading;
extern unsigned long lastSensorRead;

// ?????? Objects ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
FlowMonitor monitor;
EdgeInference edgeAI;
EdgeMirror mirror;

// ?????? Timing ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
unsigned long lastSample    = 0;
unsigned long lastReset     = 0;
unsigned long lastHeartbeat = 0;

// ?????? Counters ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
unsigned long totalFlows    = 0;
unsigned long totalAttacks  = 0;
unsigned long totalNormal   = 0;
unsigned long totalBlocked  = 0;
unsigned long tinymlSkipped = 0;
unsigned long inferenceCount = 0;

// ?????? Blocking state ???????????????????????????????????????????????????????????????????????????????????????
char lastMqttCommandMsg[512] = {0};

bool snifferOk = false;

static constexpr float ZSCORE_FAST_PATH = 0.3f;

// =====================================================
void setup() {
    Serial.begin(115200);
    delay(1000);

    pinMode(LED_STATUS, OUTPUT);
    pinMode(LED_ALERT,  OUTPUT);
    pinMode(BTN_TEST,   INPUT_PULLUP);

    Serial.println("\n=== ESP32 Edge AI IDS ===");

    for (int i = 0; i < 3; i++) {
        digitalWrite(LED_STATUS, HIGH);
        digitalWrite(LED_ALERT,  HIGH);
        delay(150);
        digitalWrite(LED_STATUS, LOW);
        digitalWrite(LED_ALERT,  LOW);
        delay(150);
    }

    sensorBegin();
    patternCache.begin();
    patternCache.clear();
    connectWiFi();

    if (!wifiConnected) {
        while (true) {
            digitalWrite(LED_ALERT, HIGH); delay(300);
            digitalWrite(LED_ALERT, LOW);  delay(300);
        }
    }

    Serial.println(WiFi.localIP());
    snifferOk = startSniffer();

    if (USE_MQTT) {
        setupMQTT();
        mqttConnect();
    }

    if (!edgeAI.begin()) {
        Serial.println("[WARN] TinyML failed");
    } else {
        edgeAI.diagnostics();
    }

    lastSensorRead = millis() - SENSOR_INTERVAL_MS;
    sensorTick();

    monitor.reset();
    lastSample    = millis();
    lastReset     = millis();
    lastHeartbeat = millis();

    digitalWrite(LED_STATUS, HIGH);
}

// =====================================================
void loop() {
    unsigned long now = millis();

    maintainWiFi();
    if (USE_MQTT) maintainMQTT();

    // ?????? Process blocking commands ??????????????????????????????????????????????
    if (newCommand) {
        newCommand = false;
        BlockCommand cmd = {};
        if (parseBlockCommand(lastCommandMsg, cmd)) {
            executeBlockCommand(cmd);
        }
    }

    if (wifiConnected && !snifferOk) {
        snifferOk = startSniffer();
        monitor.reset();
        lastReset = now;
    }

    if (now - lastHeartbeat >= HEARTBEAT_MS) {
        lastHeartbeat = now;
        _printHeartbeat(now);
    }

    // ?????? Sensor ???????????????????????????????????????????????????????????????????????????????????????
    bool freshSensor = sensorTick();
    if (freshSensor && latestReading.valid) {
        if (USE_MQTT && mqttClient.connected()) {
            sensorPublishMQTT(mqttClient);
        }
    }

    // ?????? Reset window ?????????????????????????????????????????????????????????????????????
    if (now - lastReset >= FLOW_RESET_MS) {
        monitor.reset();
        lastReset = now;
    }

    // ?????? Sampling ??????????????????????????????????????????????????????????????????????????????
    if (now - lastSample < SAMPLE_MS) return;
    lastSample = now;
    totalFlows++;

    FlowFeatures f = monitor.extract();
    float z = monitor.anomalyScore(f);

    Serial.printf("[FLOW] z=%.3f\n", z);

    // ?????? Fast path ???????????????????????????????????????????????????????????????????????????
    if (z < ZSCORE_FAST_PATH) {
        totalNormal++;
        tinymlSkipped++;
        monitor.reset();
        return;
    }

    // ?????? Inference ???????????????????????????????????????????????????????????????????????????
    EdgeResult result;
    result.valid = false;

    if (edgeAI.isReady()) {
        result = edgeAI.predict(f);
        inferenceCount++;

        // Primary: trust NN completely
        result.is_attack  = (result.probability >= NN_THRESHOLD);
        result.confidence = result.probability;

        // Fallback: z-score catches what NN missed
        if (!result.is_attack && z >= Z_THRESHOLD) {
            result.is_attack  = true;
            result.confidence = min(z / 5.0f, 1.0f);
            Serial.printf("[INFER] Z-FALLBACK z=%.3f\n", z);
        }
    } else {
        // NN not ready — z-score only
        result.is_attack  = (z >= Z_THRESHOLD);
        result.confidence = min(z / 5.0f, 1.0f);
        result.valid      = true;
        Serial.println("[INFER] NN not ready — z-score only");
    }

    Serial.printf("[INFER] attack=%s conf=%.2f prob=%.2f\n",
                  result.is_attack ? "YES" : "NO",
                  result.confidence,
                  result.probability);

    // ?????? Previously blocked repeated patterns at edge; now disabled.
    if (result.is_attack && patternCache.should_block(f, z)) {
        // Log detection but do not block at edge. Central sandbox will confirm and
        // instruct the device if blocking is required.
        Serial.println("[CACHE] Pattern matched (edge block disabled) — forwarding to backend");
    }

    // ?????? Mirror ????????????????????????????????????????????????????????????????????????????????????
    MirrorResult mr = mirror.mirror(f, result, z);

    if (result.is_attack) {
        totalAttacks++;
        _blinkAlert(4);

        Serial.printf("[ATTACK] mirrored=%s\n", mr.sent ? "YES" : "NO");

        // Store in cache only if NN is confident
        if (mr.sent && result.probability >= 0.5f) {
            patternCache.add_attack(f, result.confidence, "EdgeAI");
            Serial.println("[CACHE] Stored — NN confirmed");
        } else {
            Serial.printf("[CACHE] Skipped — prob=%.2f z=%.2f\n",
                          result.probability, z);
        }
    } else {
        totalNormal++;
    }

    delay(30);
    monitor.reset();
}

// =====================================================
void _blinkAlert(int times) {
    for (int i = 0; i < times; i++) {
        digitalWrite(LED_ALERT, HIGH); delay(80);
        digitalWrite(LED_ALERT, LOW);  delay(80);
    }
}

// =====================================================
void _printHeartbeat(unsigned long now) {
    Serial.println("\n========= DEVICE STATUS =========");
    Serial.printf("Uptime: %lu sec\n",      now / 1000);
    Serial.printf("Total flows: %lu\n",     totalFlows);
    Serial.printf("Attacks: %lu\n",         totalAttacks);
    Serial.printf("Normal: %lu\n",          totalNormal);
    Serial.printf("Blocked: %lu\n",         totalBlocked);
    Serial.printf("TinyML: %lu\n",          inferenceCount);
    Serial.printf("Skipped: %lu\n",         tinymlSkipped);
    Serial.printf("WiFi RSSI: %d\n",        WiFi.RSSI());
    Serial.printf("Heap: %u\n",             ESP.getFreeHeap());
    Serial.printf("MQTT: %s\n",
        (USE_MQTT && mqttClient.connected()) ? "YES" : "NO");
    Serial.printf("TinyML ready: %s\n",
        edgeAI.isReady() ? "YES" : "NO");
    
    // Blocking statistics
    Serial.printf("\n--- Blocking Status ---\n");
    Serial.printf("Active blocks: %d\n",     blockCount);
    Serial.printf("Packets dropped: %lu\n", blockStats.packetsDropped);
    Serial.printf("Commands received: %lu\n", blockStats.commandsReceived);
    Serial.printf("Commands failed: %lu\n",  blockStats.commandsFailed);
    
    mirror.printStats();
    printBlockList();
    Serial.println("=================================\n");
}